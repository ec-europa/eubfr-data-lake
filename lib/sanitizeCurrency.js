// Common conversions
const mapping = {
  ECU: 'EUR',
  FF: 'FRF',
  DM: 'DEM',
};

// Always return a valid ISO 4217 code
const sanitizeCurrency = currency => {
  if (!currency) return '';

  if (currency in mapping) return mapping[currency];

  if (validCodes.indexOf(currency) >= 0) return currency;

  return '';
};

module.exports = sanitizeCurrency;
